
$(document).ready(function() {
  $('img').click(function(event) {
    $(this).css('transform','rotate(45deg)');
    
  });
});

$(document).ready(function() {
  $('img').dblclick(function(event) {
    $(this).css('transform','rotate(0deg)');

  });
});


                    
                    
                
                
